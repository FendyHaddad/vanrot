#!/usr/bin/env node
// Regenerates packages/cli/src/create/package-versions.ts from workspace package manifests.
import { readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';

const repoRoot = resolve(import.meta.dirname, '..');
const outputPath = resolve(repoRoot, 'packages/cli/src/create/package-versions.ts');

export const createPackageDirectories = [
  'behavior',
  'cli',
  'config',
  'forge',
  'formatters',
  'router',
  'runtime',
  'seo',
  'ui',
  'vite-plugin',
];

export async function readCreatePackageVersions(root = repoRoot) {
  const entries = await Promise.all(
    createPackageDirectories.map(async (directory) => {
      const manifestPath = resolve(root, 'packages', directory, 'package.json');
      const manifest = JSON.parse(await readFile(manifestPath, 'utf8'));

      if (typeof manifest.name !== 'string' || typeof manifest.version !== 'string') {
        throw new Error(`packages/${directory}/package.json is missing name or version.`);
      }

      return [manifest.name, manifest.version];
    }),
  );

  return Object.fromEntries(entries.sort(([left], [right]) => left.localeCompare(right)));
}

export function renderCreatePackageVersions(versions) {
  const lines = Object.entries(versions).map(([name, version]) => `  '${name}': '${version}',`);

  return [
    '// Generated by scripts/generate-create-package-versions.mjs. Do not edit by hand.',
    '// Run: node scripts/generate-create-package-versions.mjs',
    'export const createPackageVersions = {',
    ...lines,
    '} as const;',
    '',
    'export type CreatePackageName = keyof typeof createPackageVersions;',
    '',
    'export function createRegistryDependencyVersion(name: CreatePackageName): string {',
    '  return `^${createPackageVersions[name]}`;',
    '}',
    '',
  ].join('\n');
}

const invokedDirectly = process.argv[1] !== undefined
  && resolve(process.argv[1]) === resolve(import.meta.dirname, 'generate-create-package-versions.mjs');

if (invokedDirectly) {
  const versions = await readCreatePackageVersions();
  await writeFile(outputPath, renderCreatePackageVersions(versions), 'utf8');
  console.log(`Wrote ${outputPath}`);
}
